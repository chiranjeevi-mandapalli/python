# None
# {}
# ''
# []
# ()
# by default these conditions are treated as false
if False:
    print("Is true")
else:
    print("Is False")

if None:
    print("Is true")
else:
    print("Is False")

if {}:
    print("Is true")
else:
    print("Is False")

if '':
    print("Is true")
else:
    print("Is False")

if []:
    print("Is true")
else:
    print("Is False")

if ():
    print("Is true")
else:
    print("Is False")